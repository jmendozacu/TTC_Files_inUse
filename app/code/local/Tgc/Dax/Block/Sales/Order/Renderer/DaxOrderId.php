class Tgc_Dax_Block_Sales_Order_Renderer_DaxOrderId  extends Mage_Adminhtml_Block_Widget_Grid_Column_Renderer_Abstract
{
    public function render(Varien_Object $row)
    {
        $value = $row->getData($this->getColumn()->getIndex());

        return $value == 0 ? 'Not Yet Received' : $value;
    }
}
